package Basics;

public class Array {
	public static void main(String[] args) {
//		Static way insert the value directly
//		DataType VarName []= {e1,e2,e3 .....}; elements
		int a[] = {10,20,30,40};
		System.out.println("Single element "+a[0]);
		for (int i=0;i<a.length;i++) {
			System.out.println(a[i]);
		}

}
}
